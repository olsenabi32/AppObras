    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = TextNombreTrabajador.getText();
        String descObra = TextDescripObra.getText();
        String fecha = TextFormatFechaServicio.getText().replace("-", ""); // Limpiar formato
        String hIni = jFormattedHInicio.getText();
        String hFin = jFormattedHFin.getText();

        try {
            
            String nif = qryTrab.obtenerNifPorNombre(nombre);
            String cod = qryObra.obtenerCodPorDesc(descObra);

            //comprobar q la obra y trabajador existen
            if (nif.isEmpty() || cod.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error: El trabajador o la obra no existen. "
                    + "Asegúrate de escribir el nombre exacto.", "Error de integridad", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Valido superposición
            if (DMServicios.existeSuperposicion(nif, fecha, hIni, hFin)) {
                JOptionPane.showMessageDialog(this, "¡Error! El trabajador ya tiene un servicio en ese horario.");
                return;
            }
            
            if (DMServicios.insertarServicio(nif, cod, fecha, hIni, hFin)) {
                JOptionPane.showMessageDialog(this, "Servicio agregado.");
                cargarTabla();
                limpiarCampos();             
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

