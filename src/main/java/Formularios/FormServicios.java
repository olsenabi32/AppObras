    private void cargarTabla() {
    try {
        List<DServicioDetallado> lista = DMServicios.QryCargaServiciosDetallados();
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[]{"Trabajador", "Obra", "Fecha", "H. Inicio", "H. Fin"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) { return false; }
        };

        for (DServicioDetallado s : lista) {
            modelo.addRow(new Object[]{s.nombreTrabajador, s.descripObra, s.fecha, s.horaInicio, s.horaFin});
        }
        jTable1.setModel(modelo);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar servicios: " + e.getMessage());
    }
}
    
