    private void cargarTrabajadores(){
          qry = new DMTrabajadores();
        
        try {
            ListTrabajadores = qry.QryCargaTrabajadores();
                    } catch (SQLException ex) {
            Logger.getLogger(FAutenticar.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Se ha cargado el Array List de Trabajadores");
        for (DSTrabajadores Cursor : ListTrabajadores)
           {
               this.descripcion = Cursor.getDescripcion();
               this.CBTrabajadores.addItem(descripcion);
            }

    }

