    private void BEntrarAutenticacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEntrarAutenticacionActionPerformed
        // TODO add your handling code here:
        contraseña = new String(jPasswordField1.getPassword());
        String nombreSeleccionado = (String) CBTrabajadores.getSelectedItem();

        
        System.out.println("Intentando entrar con: " + nombreSeleccionado);
        System.out.println("Total trabajadores en lista: " + ListTrabajadores.size());

        boolean encontrado = false;

        for (DSTrabajadores t : ListTrabajadores) {
            // Comparamos la descripción para identificar al trabajador
            // y el NIF para la contraseña 
            if (t.getDescripcion().equals(nombreSeleccionado)) {
                System.out.println("Usuario localizado. Validando contraseña...");

                if (t.getNif().equals(contraseña)) { 
                    usuario = t;
                    autentificar = true;
                    encontrado = true;
                    System.out.println("Autenticación exitosa para: " + t.getNombre());
                    break; // Salimos del bucle si ya lo encontramos
                } else {
                    System.out.println("Contraseña incorrecta para el usuario.");
                }
            }
        }

        if (encontrado) {
            this.dispose();                
            new FMenPrincipal().setVisible(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", 
                    "Error de acceso", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
 
    }//GEN-LAST:event_BEntrarAutenticacionActionPerformed

