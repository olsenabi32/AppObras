    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) { // Doble clic
            int fila = jTable1.getSelectedRow();
                if (fila != -1) {
                // Extraer datos de la fila y ponerlos en los campos
                TextDescripcion.setText(jTable1.getValueAt(fila, 0).toString());
                TextNombre.setText(jTable1.getValueAt(fila, 1).toString());
                TextPoblacion.setText(jTable1.getValueAt(fila, 2).toString());
                TextPvp.setText(jTable1.getValueAt(fila, 3).toString());
                TextTipo.setText(jTable1.getValueAt(fila, 4).toString());
                
                // Bloquear campos clave
                TextDescripcion.setEditable(false);
                TextNIF.setEditable(false);
                
                // Habilitar botones de acción
                btnEliminar.setEnabled(true);
                btnActualizar.setEnabled(true);
                }
        }
    }//GEN-LAST:event_jTable1MouseClicked

