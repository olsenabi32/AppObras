    private void cargarTabla() {
    try {
        List<DSTrabajadores> lista = Objqry.QryCargaTrabajadores();
        DefaultTableModel TablaT = new DefaultTableModel(
            new Object[]{"Descripción", "Nombre", "Población", "PVP Hora", "Tipo"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Esto hace que TODAS las celdas sean no editables
                return false;
            }
        };

        for (DSTrabajadores t : lista) {
            Object[] fila = new Object[5];
            fila[0] = t.getDescripcion(); 
            fila[1] = t.getNombre();
            fila[2] = t.getPoblacion();
            fila[3] = t.getPVP_Hora();
            fila[4] = (t.getTipo() == 1) ? "Administrativo" : "Servicios";
            TablaT.addRow(fila);
        }
        jTable1.setModel(TablaT);
    } catch (Exception e) {
        System.err.println("Error al cargar tabla: " + e.getMessage());
    }
} 
    
