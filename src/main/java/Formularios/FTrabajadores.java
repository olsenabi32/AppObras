    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        String seleccion = (String) jComboBox1.getSelectedItem();
        MaskFormatter formatter = null;

        try {
            switch (seleccion) {
                case "NIF":
                formatter = new MaskFormatter("########A"); // 8 números + 1 letra
                break;
                case "NIE":
                formatter = new MaskFormatter("U########A"); // Letra inicial + 7 números + letra final
                break;
                case "CIF":
                formatter = new MaskFormatter("U########"); // Letra inicial + 8 números
                break;
                case "Libre":
                formatter = new MaskFormatter("********************"); // Sin formato fijo
                break;
            }

            if (formatter != null) {
                formatter.setPlaceholderCharacter('_');
                TextNIF.setFormatterFactory(
                    new javax.swing.text.DefaultFormatterFactory(formatter)
                );
                TextNIF.setValue(null); // Limpia el campo al cambiar formato
            }
        } catch (java.text.ParseException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        // TODO add your handling code here:
        String nif = TextNIF.getText().trim(); 
        String nombre = TextNombre.getText().trim();
        String poblacion = TextPoblacion.getText().trim();
        int pvp = Integer.parseInt(TextPvp.getText());
        String tipoStr = TextTipo.getText().trim(); 

        if (nif.isEmpty() || nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "NIF y Nombre son obligatorios.");
            return;
        }

        // Llamamos al método de inserción del módulo de datos
        boolean exito = Objqry.insertarTrabajador(nif, nombre, poblacion, pvp, tipoStr);

        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "Trabajador añadido con éxito.");
            cargarTabla(); 
            limpiarCampos();
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        String nif = TextNIF.getText(); 
        String nombre = TextNombre.getText();
        String poblacion = TextPoblacion.getText();
        int pvp = Integer.parseInt(TextPvp.getText());
        int tipo = TextTipo.getText().equals("Administrativo") ? 1 : 0; 

        // Usamos el método de modificación
        boolean exito = Objqry.modificarTrabajador(nif, nombre, poblacion, pvp, tipo);

        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "Datos actualizados.");
            cargarTabla();
            limpiarCampos();
            // Al terminar, volvemos a deshabilitar botones de edición
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
            TextNIF.setEditable(true);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String nif = TextNIF.getText().trim();
    
        // Validación de seguridad: no borrar al usuario actual
        if(user.getNif().equals(nif)){
            javax.swing.JOptionPane.showMessageDialog(this, "No puedes eliminar al usuario conectado actualmente.");
            limpiarCampos();
            return;
        }

        int confirmar = javax.swing.JOptionPane.showConfirmDialog(this, 
                "¿Estás seguro de eliminar a este trabajador?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
            if (Objqry.eliminarTrabajador(nif)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Trabajador eliminado.");
                cargarTabla();
                limpiarCampos();
                btnEliminar.setEnabled(false);
                btnActualizar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

