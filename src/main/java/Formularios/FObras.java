    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
          int fila = jTable1.getSelectedRow();
          if (fila != -1) {
            // Obtenemos los datos reales de la lista interna, no de la tabla
            DSObras obra = listaObras.get(fila);
            // Guardamos el código 
            codigoSeleccionado = obra.getCodigo();
            
             // Rellenamos los campos visibles
            TextDescripcion.setText(obra.getDescripcion());
            jFormattedTextField1.setText(obra.getFecha_Termina());
            TextDescuento.setText(String.valueOf(obra.getDescuento()));
            
            //pongo el codigo pero no dejo que se edite
            TextCodigo.setText(codigoSeleccionado); 
            TextCodigo.setEditable(false); 
            btnActualizar.setEnabled(true); 
            btnEliminar.setEnabled(true); 
        }
      }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed

        String codigo = TextCodigo.getText().trim();
        String desc = TextDescripcion.getText().trim();
        String fecha = jFormattedTextField1.getText().trim();
        String descuentoStr = TextDescuento.getText().trim();
        int descue = 0;

        
        if (codigo.isEmpty() || fecha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "El Código de Obra y la fecha son obligatorios para crear un registro.", 
                "Campos Incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        try {
            if (!descuentoStr.isEmpty()) {
                descue = Integer.parseInt(descuentoStr);
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "El descuento debe ser un valor numérico entero.", 
                "Error de Formato", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        if (Objqry.insertarObra(codigo, fecha, descue, desc)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Obra '" + desc + "' registrada con éxito.", 
                "Operación Exitosa", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            cargarTabla();
            limpiarCampos(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error al insertar: Es posible que el código '" + codigo + "' ya exista en el sistema.", 
                "Error de Duplicidad", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAñadirActionPerformed

