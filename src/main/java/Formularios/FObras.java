    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        //comprueba que no tenga servicios antes de borrarla
        if (Objqry.codigoEnServicio(codigoSeleccionado)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Privacidad: Esta obra tiene servicios y no puede borrarse.");
            limpiarCampos();
            return; 
        }

        if (Objqry.eliminarObra(codigoSeleccionado)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Registro eliminado.");
            cargarTabla();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

