    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        // TODO add your handling code here:
        try {
            TODOTrabajadores = Objqry.QryCargaTodaInfoTrabajadores();
                    } catch (SQLException ex) {
            Logger.getLogger(FAutenticar.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Tamaño lista: " + TODOTrabajadores.size());
            for (DSTrabajadores t : TODOTrabajadores) {
            System.out.println(" -> " + t.getNombre());
        }
        
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
        seleccion = (String) jComboBox2.getSelectedItem();
        if (seleccion == null || seleccion.isEmpty()) return;

        for (DSTrabajadores trab : TODOTrabajadores) {
            if (trab.getNombre().equals(seleccion)) {
                // Cargar los datos en los campos
                
                TextNombre2.setText(trab.getNombre());
                NIFTexto.setText(trab.getNif());
                TextPobla2.setText(trab.getPoblacion());
                jSpinner2.setValue(trab.getPVP_Hora());
                
                if (trab.getTipo() == 1) {  // o el valor que uses para "admin"
                    jRadioButton1.setSelected(true);
                } else {
                    jRadioButton2.setSelected(true);
                }

                break;
            }
        }
    }
    }//GEN-LAST:event_jComboBox2ItemStateChanged

