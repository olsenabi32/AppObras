    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        // TODO add your handling code here:
        String nombre= TextNombre2.getText();
        String nif= NIFTexto.getText();
        String poblacion = TextPobla2.getText().trim();
        int tipo = 0;
        int pvp = (int) jSpinner1.getValue();
        System.out.println(pvp);

    
    // Determinar el tipo según el radio button
    if (ButtomAdmin.isSelected()) {
        tipo = 1;
    } 

    // Validar datos mínimos
    if (nif.isEmpty() || poblacion.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Debes completar todos los campos.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }


    // Llamar al método de inserción
    boolean exito = Objqry.modificarTrabajador(nif,nombre, poblacion,pvp, tipo);

    // Mostrar resultado
    if (exito) {
        javax.swing.JOptionPane.showMessageDialog(this, " guardado correctamente.", 
            "Éxito", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Limpiar campos
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "No se pudo guardar el trabajador.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_botonModificarActionPerformed

    private void BottonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottonEliminarActionPerformed
        // TODO add your handling code here:
        String nif = NIFTexto.getText().trim();
        if(user.getNif().equals(nif)){
            javax.swing.JOptionPane.showMessageDialog(this, "Estas borrando el usuario acutal.", 
            "Éxito", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            user=null;
            new FMenPrincipal().setVisible(true);
            
        }
     
    // Llamar al método de inserción
    boolean exito = Objqry.eliminarTrabajador(nif);

    // Mostrar resultado
    if (exito) {
        javax.swing.JOptionPane.showMessageDialog(this, " Eliminado correctamente.", 
            "Éxito", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Limpiar campos
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "No se pudo eliminar el trabajador.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_BottonEliminarActionPerformed

