    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nif = TextNIF.getText().trim();
        String nombre = TextNombre.getText().trim();
        String poblacion = TextPobla.getText().trim();
        String tipo = "";
        int pvp = (int) jSpinner1.getValue();
        System.out.println(pvp);

    
    // Determinar el tipo según el radio button
    if (ButtomAdmin.isSelected()) {
        tipo = "Administrativo";
    } else if (ButtomServicios.isSelected()) {
        tipo = "Servicios";
    }

    // Validar datos mínimos
    if (nif.isEmpty() || nombre.isEmpty() || poblacion.isEmpty() || tipo.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Debes completar todos los campos.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }



    // Llamar al método de inserción
    boolean exito = Objqry.insertarTrabajador(nif, nombre, poblacion,pvp, tipo);

    // Mostrar resultado
    if (exito) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            nombre + " guardado correctamente.", 
            "Éxito", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Limpiar campos
        TextNIF.setValue(null);
        TextNombre.setText("");
        TextPobla.setText("");
        buttonGroup1.clearSelection();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "No se pudo guardar el trabajador.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

