    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        // TODO add your handling code here:
        
        String Codigo = TextCodigo.getText().trim();
        String FechaTer = TextFechaTer.getText().trim();
        int Descuento = (int)Descuento1.getValue();
        String Descripcion = TextDescripcion.getText().trim();
 

    // Validar datos mínimos
    if (Codigo.isEmpty() || FechaTer.isEmpty() || Descripcion.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Debes completar todos los campos.", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

   

    // Llamar al método de inserción
    boolean exito = Objqry.insertarObra(Codigo, FechaTer, Descuento,Descripcion);

    // Mostrar resultado
    if (exito) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Insertado correctamente.", 
            "Éxito", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Limpiar campos
        
        TextFechaTer.setText("");
        TextCodigo.setText("");
        TextDescripcion.setText("");
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "No se pudo guardar la obra .", 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        
        
    }//GEN-LAST:event_botonInsertarActionPerformed

