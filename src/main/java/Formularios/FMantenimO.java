    private void comboxCodigoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboxCodigoItemStateChanged
        // TODO add your handling code here:
         try {
            InfoObras = Objqry.QryCargaTodaInfoObras();
                    } catch (SQLException ex) {
            Logger.getLogger(FAutenticar.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Tamaño lista: " + InfoObras.size());
            for (DSObras t : InfoObras) {
            System.out.println(" -> " + t.getCodigo());
        }
        
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
        String codigo_obra = (String) comboxCodigo.getSelectedItem();
        if (codigo_obra == null ) return;

        for (DSObras obra : InfoObras) {
            if (obra.getCodigo().equals(codigo_obra)) {
                // Cargar los datos en los campos
                DescripText2.setText(obra.getDescripcion());
                FechaText2.setText(obra.getFecha_Termina());
                Descuento2.setValue(obra.getDescuento());

                break;
            }
        }
    }
    }//GEN-LAST:event_comboxCodigoItemStateChanged

    public void Aliniciar(){ 
        try {
            InfoObras = Objqry.QryCargaTodaInfoObras();
                    } catch (SQLException ex) {
            Logger.getLogger(FAutenticar.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Se ha cargado el Array List de Trabajadores");
        for (DSObras Cursor : InfoObras)
           {
               String codigo = Cursor.getCodigo();
               comboxCodigo.addItem(codigo);
            }
    }
    
    
